let dia = "quinta";

switch (dia) {
    case "segunda":
        console.log("Vou tomar café");
        break;
    case "terca":
        console.log("Vou ficar em casa");
        break;
     case "quarta":
        console.log("Vou no cinema");
        break;
     case "quinta":
        console.log("Vou tocar violão");
        break;
    case "sexta":
        console.log("Vou na chopperia");
        break;
    case "sabado":
        console.log("Vou no shopping");
        break;
    case "domingo":
        console.log("Vou na praia");
        break;

    default:
        console.log("Não foi escolhido nenhum dia da semana.");
        break;
}